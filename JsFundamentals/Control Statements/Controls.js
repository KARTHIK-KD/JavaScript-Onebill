// if Condition
var i=10;
if(i==10){
    console.log("true");
}

//if else 
var i=10;
if(i==10){
    console.log("true");
}
else{
    console.log("false");
}

//nested if else
var i=10;
if(i==10){
    console.log("10");
}
else if(i==9){
    console.log("9");
}
else{
    console.log("not matiching");
}

